SELECT product_id 
FROM Products 
WHERE low_fats='Y' AND recyclable = 'Y';

-- The above query filters the 'Products' table using the 'WHERE' clause and the 'AND' operator to find products that are both low fat and recyclable, based on the specified criteria.

